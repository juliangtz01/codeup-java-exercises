public class StringExercise
{
    public static void main(String[] args)
    {
        String value1 = "We don't need no education";
        System.out.printf("%s%n", value1);
        System.out.printf("%s%n",value1.replace("education", "thought control"));
        String value2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.printf("%s%n",value2);
        String value3 = "In windows, the main drive is usually c:\\";
        System.out.printf("%s%n",value3);
        String value4 = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\";
        System.out.printf("%s%n",value4);

    }
}
